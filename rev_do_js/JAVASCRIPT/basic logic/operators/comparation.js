// COMPARAÇÃO

//  > Maior que
//  < Menor que
//  >= Maior ou igual a 
//  <= Menor ou igual a 
// == (comparação de valores) Igual a 
// === (comparação de tipo de variável) Estritamente igual a 
// != Diferente de 
// !== Estritamente diferente de

// Exemplo 1
console.log(50 > 52);

// Exemplo 2
console.log(18 < 20);

// Exemplo 3
console.log(4 >= 3)
console.log(4 >= 5)

// Exemplo 4
console.log(10 <= 11)
console.log(10 <= 10)
console.log(10 <= 9)

// Exemplo 5
console.log(2 == 2)
console.log(2 == 3)
console.log(2 != 3)

// Exemplo 6 
console.log("2" === 2) // false, tipos diferentes, string e number

// Exemplo 7
console.log("Rincko" == "Rincko") //true
console.log("Rincko" == "rincko") //false, pois a primeira letra de cada um é diferente
console.log("Rincko" !== "Juliano") //true

// Exemplo 7
const action = "add"
console.log(action === "add")
console.log(action === "remove")

// Exemplo 8
console.log(10 * 3 == 30)
console.log("O número 4 é par?", (4 % 2) == 0)

 